// 1. Using var
var name = "Umang";
var age = 20;

console.log("Name:", name);
console.log("Age:", age);

// 2. Using let
let city = "Mumbai";
console.log(city);

city = "Delhi";   // value can be changed
console.log(city);
// 3. Using const
const pi = 3.14159;
console.log("PI value:", pi);

// 4. String + Variables
let username = "Aarav";
let score = 95;

console.log("Student " + username + " scored " + score + " marks.");
//  5. Undefined & Null
let data;
console.log(data);  // undefined

let emptyValue = null;
console.log(emptyValue); // null
